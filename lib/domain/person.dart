class Person {
  final String id;
  final String name;
  final int age;
  final String gender;
  final String phoneNumber;
  final String email;
  final String address;

  Person({
    required this.id,
    required this.name,
    required this.age,
    required this.gender,
    required this.phoneNumber,
    required this.email,
    required this.address,
  });
}
